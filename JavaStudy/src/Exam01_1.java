
public class Exam01_1 {
	public static void main(String[] args) {
		int n1 = 1;
		int n2 = 2;
		int n3 = 3;
		int n4 = 4;
		int n5 = 5;

		System.out.println(n1 + "+" + n2 + "+" + n3 + "의 연산 결과는 " + "'" + (n1 + n2 + n3) + "'" + " 입니다.");
		System.out.println(n1 + "+" + n2 + "+" + n3 + "의 연산 결과는 " + "\"" + (n1 + n2 + n3) + "\"" + " 입니다.");
		System.out.println(n1 + "~" + n5 + "까지의 곱셈 결과는 " + (n1 * n2 * n3 * n4 * n5) + " 입니다.");
	}
}
